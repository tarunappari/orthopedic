export const NavItems = ({ items, className, onItemClick }) => {
  const [hovered, setHovered] = useState(null);
  const pathname = usePathname();

  return (
    <motion.div
      onMouseLeave={() => setHovered(null)}
      className={cn(
        "absolute inset-0 hidden flex-1 flex-row items-center justify-center space-x-2 text-sm font-medium text-zinc-600 transition duration-200 hover:text-zinc-800 lg:flex lg:space-x-2",
        className
      )}
    >
      {items.map((item, idx) => {
        const isActive = pathname === item.link;
        return (
          <a
            onMouseEnter={() => setHovered(idx)}
            onClick={onItemClick}
            className="relative px-4 py-2 text-white transition-all duration-300 hover:text-blue-500"
            key={`link-${idx}`}
            href={item.link}
          >
            {(hovered === idx || isActive) && (
              <motion.div
                layoutId={isActive ? "active" : "hovered"}
                className={`absolute inset-0 h-full w-full rounded-full ${
                  isActive
                    ? "bg-blue-500/20 border border-blue-500/30"
                    : "bg-white/10 border border-white/20"
                }`}
              />
            )}
            <span
              className={`relative z-20 font-medium ${
                isActive ? "text-blue-400 font-semibold" : "text-white"
              } transition-all duration-300`}
            >
              {item.name}
            </span>
          </a>
        );
      })}
    </motion.div>
  );
};

